import string
from random import choice, randint


def rand_str():
    mystr = []
    for x in range(3):
        chars = tuple(string.ascii_letters + string.digits)
        text = (choice(chars) for _ in range(randint(7, 14)))
        text = ''.join(text)
        mystr.append(text)
    return '&'.join(mystr)


